#!/usr/bin/env node
/**
 * Generate Railway-ready env file with secure JWT secrets.
 * Usage: node scripts/setup-production.mjs
 *        node scripts/setup-production.mjs --database-url "mongodb+srv://..."
 */
import crypto from "crypto";
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const outDir = path.join(root, "deploy");
const outFile = path.join(outDir, "railway.env.generated");

const args = process.argv.slice(2);
let databaseUrl = "";
let appUrl = "";

for (let i = 0; i < args.length; i++) {
  if (args[i] === "--database-url" && args[i + 1]) {
    databaseUrl = args[++i];
  }
  if (args[i] === "--app-url" && args[i + 1]) {
    appUrl = args[++i].replace(/\/$/, "");
  }
}

const jwtSecret = crypto.randomBytes(64).toString("hex");
const jwtRefresh = crypto.randomBytes(64).toString("hex");

const lines = [
  "# Generated by npm run setup:deploy — paste into Railway → Variables",
  "# Platform: Railway + MongoDB Atlas (recommended)",
  "",
  "NODE_ENV=production",
  "HOST=0.0.0.0",
  "ENABLE_DEMO_AUTH=false",
  "",
  `JWT_SECRET=${jwtSecret}`,
  `JWT_REFRESH_SECRET=${jwtRefresh}`,
  "",
  databaseUrl
    ? `DATABASE_URL=${databaseUrl}`
    : "# DATABASE_URL=mongodb+srv://USER:PASS@cluster.mongodb.net/tra-da-mentor?retryWrites=true&w=majority",
  "",
  "# Optional on Railway — auto-detected from RAILWAY_PUBLIC_DOMAIN if omitted:",
  appUrl ? `CORS_ORIGIN=${appUrl}` : "# CORS_ORIGIN=https://YOUR-APP.up.railway.app",
  appUrl ? `FRONTEND_URL=${appUrl}` : "# FRONTEND_URL=https://YOUR-APP.up.railway.app",
  appUrl ? `BASE_URL=${appUrl}` : "# BASE_URL=https://YOUR-APP.up.railway.app",
  "",
  "# Optional integrations",
  "# SENDGRID_API_KEY=",
  "# STRIPE_SECRET_KEY=",
  "# STRIPE_WEBHOOK_SECRET=",
  "# GOOGLE_CLIENT_ID=",
  "# GOOGLE_CLIENT_SECRET=",
  "",
];

fs.mkdirSync(outDir, { recursive: true });
fs.writeFileSync(outFile, lines.join("\n") + "\n");

console.log("\n✅ Quyết định deploy: Railway + MongoDB Atlas\n");
console.log("📄 File đã tạo:", path.relative(root, outFile));
console.log("\n--- Làm 3 bước sau ---\n");
console.log("1) Atlas: https://www.mongodb.com/atlas → cluster free → Connect → copy URI");
console.log("2) Railway: https://railway.app → New Project → Deploy from GitHub → chọn repo này");
console.log("3) Railway → Variables → Raw Editor → dán nội dung file trên (+ DATABASE_URL từ Atlas)\n");
console.log("Sau deploy mở: https://<tên-app>.up.railway.app/api/health");
console.log("Mong đợi: storage=mongodb, status=ok\n");
console.log("Chi tiết: DEPLOY.md\n");
