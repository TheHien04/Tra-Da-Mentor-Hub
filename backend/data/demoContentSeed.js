/** daysAgo for invite createdAt */
export function buildInviteDemo(baseUrl) {
  const daysAgo = (n) => new Date(Date.now() - n * 24 * 60 * 60 * 1000).toISOString();
  return [
    {
      email: 'new.mentee@example.com',
      role: 'mentee',
      createdAt: daysAgo(1),
    },
    {
      email: 'senior.dev@company.com',
      role: 'mentor',
      createdAt: daysAgo(3),
    },
    {
      email: 'admin.ops@trada.edu.vn',
      role: 'admin',
      createdAt: daysAgo(5),
    },
    {
      email: 'expired.user@example.com',
      role: 'mentee',
      createdAt: daysAgo(10),
    },
  ].map((row) => {
    const token = crypto.randomBytes(16).toString('hex');
    return { token, ...row, link: `${baseUrl}/register?invite=${token}` };
  });
}
